let result = prompt("enter your number");

if (result >= 90) {

    console.log("A+ Grade");

}

else if (result >= 80) {
    console.log("A Grade");
}

else if (result >= 70) {
    console.log("B+ Grade");
}
else if (result >=60){
    console.log("B Grade");
}

else if (result >= 50) {
    console.log("C Grade");
}
else if (result <50){
    console.log("Fail");
}

